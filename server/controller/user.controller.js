export const updateProfile = async (req, res) => {
    try {
        const { fullname, email, phoneNumber, bio, skills } = req.body;
        
        const file = req.file;
        let cloudResponse;

        // ðŸŸ¢ FIX: Upload logic that keeps the real filename
        if (file) {
            const fileUri = getDataUri(file);
            cloudResponse = await cloudinary.uploader.upload(fileUri.content, {
                resource_type: 'auto', 
                folder: 'career_portal_resumes', 
                
                // Use the original name (without extension) as the ID
                public_id: file.originalname.split('.')[0], 
                
                use_filename: true,
                unique_filename: false, // Don't add random numbers
                overwrite: true         // Allow updating the file
            });
        }

        let skillsArray;
        if(skills){
            skillsArray = skills.split(",");
        }
        
        const userId = req.id; // middleware authentication
        let user = await User.findById(userId);

        if (!user) {
            return res.status(400).json({
                message: "User not found.",
                success: false
            })
        }
        
        // Updating data
        if(fullname) user.fullname = fullname;
        if(email) user.email = email;
        if(phoneNumber)  user.phoneNumber = phoneNumber;
        if(bio) user.profile.bio = bio;
        if(skills) user.profile.skills = skillsArray;
        
        // Resume saving logic
        if(cloudResponse){
            user.profile.resume = cloudResponse.secure_url; 
            user.profile.resumeOriginalName = file.originalname; 
        }

        await user.save();

        user = {
            _id: user._id,
            fullname: user.fullname,
            email: user.email,
            phoneNumber: user.phoneNumber,
            role: user.role,
            profile: user.profile
        }

        return res.status(200).json({
            message: "Profile updated successfully.",
            user,
            success: true
        })
    } catch (error) {
        console.log(error);
        return res.status(500).json({
            message: "Internal server error",
            success: false
        })
    }
}